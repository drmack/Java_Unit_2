public class LineBreak
{
    public static void main(String[] args) {
        run();
    }
    
    public static void run()
    {
        System.out.println("Part 1");
        printLineBreak();
        
        System.out.println("Part 2");
        printLineBreak();
    }
    
    private static void printLineBreak()
    {
        System.out.println("==========");
        System.out.println("**********");
    }
}